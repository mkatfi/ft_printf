#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
		return (write(1, &"-2147483648", 11));
	if (n < 0)
	{
		i += ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
		i += ft_putnbr(n / 10);
	i += write(1, &"0123456789"[n % 10], 1);
	return (i);
}
