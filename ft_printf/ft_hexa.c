#include "ft_printf.h"

int	ft_hexa(unsigned int n, char m)
{
	int	i;

	i = 0;
	if (n >= 16)
	{
		i += ft_hexa(n / 16, m);
		i += ft_hexa(n % 16, m);
	}
	else if (m == 'x' || m == 'p')
		i += write(1, &"0123456789abcdef"[n % 16], 1);
	else if (m == 'X')
		i += write(1, &"0123456789ABCDEF"[n % 16], 1);
	return (i);
}
